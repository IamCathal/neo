function getCrawlingUserWhenAvailable() {
    return new Promise((resolve, reject) => {
    let interval = setInterval(() => {
        if (usersCrawledIsMoreThanZero()) {
            clearInterval(interval);
            getUser(crawlID).then(user => {
                console.log(`got user`)
                console.log(user)
                setUserCardDetails(user)
                resolve(true)
            }, err => {
                reject(err)
            })
        }
      }, 50);
    });
}

function usersCrawledIsMoreThanZero() {
    if (parseInt(document.getElementById("usersCrawled").textContent) >= 1) {
        return true
    }
    return false
}

function getCrawlIDs() {
    const URLarr = window.location.href.split("/");
    let firstCrawlID = URLarr[URLarr.length-1];

    const queryParams = new URLSearchParams(window.location.search);
    const secondCrawlID = queryParams.get("secondCrawlID")

    if (secondCrawlID != undefined) {
        firstCrawlID = firstCrawlID.split("?")[0]
        return [firstCrawlID, secondCrawlID]
    } else {
        return [firstCrawlID]
    }
}

function getUser(crawlID) {
    return new Promise((resolve, reject) => {
        console.log("get user")
        fetch(`http://localhost:2590/api/getcrawlinguser/${crawlID}`, {
            headers: {
                "Content-Type": "application/json"
            },
        }).then((res => res.json()))
        .then(data => {
            if (data.error) {
                reject(data)
            }
            resolve(data.user)
        }).catch(err => {
            console.error(err);
        })
    })
}

